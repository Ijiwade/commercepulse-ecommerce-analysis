-- CommerPulse Tables 
-- customers Table 
CREATE TABLE customers (
customer_id INTEGER PRIMARY KEY,
name TEXT,
signup_date DATE,
country TEXT,
segment TEXT
); 

-- products 
CREATE TABLE products ( 
product_id INTEGER PRIMARY KEY, 
product_name TEXT, 
category TEXT, 
unit_cost REAL, 
unit_price REAL
); 

-- orders 
CREATE TABLE orders (
order_id INTEGER PRIMARY KEY,
customer_id INTEGER,
order_date DATE,
channel TEXT
); 

-- order_items 
CREATE TABLE order_items (
order_item_id INTEGER PRIMARY KEY,
order_id INTEGER,
product_id INTEGER,
quantity INTEGER,
discount_pct REAL
); 

-- refunds 
CREATE TABLE refunds (
refund_id INTEGER PRIMARY KEY,
order_id INTEGER,
refund_date DATE,
refund_amount REAL,
reason TEXT
);

